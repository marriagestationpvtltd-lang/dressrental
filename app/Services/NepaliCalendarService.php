    /**
     * The AD date corresponding to BS epoch year/month/day (2000/1/1).
     * Calibrated so that BS 2081 Baisakh 1 = AD 2024-04-13, matching the
     * official Nepal government calendar published by Nepal Rastra Bank.
     */
    protected static string $adEpoch = '1943-04-21';
    protected static int $bsEpochYear = 2000;

    public static function adToBs(int $year, int $month, int $day): array
    {
        $startDate  = Carbon::parse(self::$adEpoch);
        $targetDate = Carbon::create($year, $month, $day);
        $totalDays  = (int) $startDate->diffInDays($targetDate, false);

        if ($totalDays < 0) {
            throw new \InvalidArgumentException('Date before supported range.');
        }

        $bsYear  = self::$bsEpochYear;
        $bsMonth = 1;
        $bsDay   = 1;

        foreach (self::$bsData as $yr => $months) {
            if ($yr < self::$bsEpochYear) {
                continue;
            }
            $yearTotal = array_sum($months);
            if ($totalDays < $yearTotal) {
                $bsYear = $yr;
                break;
            }
            $totalDays -= $yearTotal;
        }

        foreach (self::$bsData[$bsYear] as $m => $days) {
            if ($totalDays < $days) {
                $bsMonth = $m + 1;
                $bsDay   = $totalDays + 1;
                break;
            }
            $totalDays -= $days;
        }

        return ['year' => $bsYear, 'month' => $bsMonth, 'day' => $bsDay];
    }

    public static function bsToAd(int $bsYear, int $bsMonth, int $bsDay): Carbon
    {
        if (!isset(self::$bsData[$bsYear])) {
            throw new \InvalidArgumentException("BS year {$bsYear} is not supported.");
        }

        $totalDays = 0;

        foreach (self::$bsData as $yr => $months) {
            if ($yr < self::$bsEpochYear) {
                continue;
            }
            if ($yr >= $bsYear) {
                break;
            }
            $totalDays += array_sum($months);
        }

        for ($m = 0; $m < $bsMonth - 1; $m++) {
            $totalDays += self::$bsData[$bsYear][$m];
        }

        $totalDays += $bsDay - 1;

        return Carbon::parse(self::$adEpoch)->addDays($totalDays);
    }

    public static function todayBs(): array
    {
        $today = Carbon::today();
        return self::adToBs($today->year, $today->month, $today->day);
    }

    public static function formatBs(int $year, int $month, int $day, bool $nepali = false): string
    {
        if ($nepali) {
            return self::toNepaliNumerals($year) . '-'
                . self::toNepaliNumerals(str_pad($month, 2, '0', STR_PAD_LEFT)) . '-'
                . self::toNepaliNumerals(str_pad($day, 2, '0', STR_PAD_LEFT));
        }
        return sprintf('%04d-%02d-%02d', $year, $month, $day);
    }

    public static function formatBsFull(int $year, int $month, int $day): string
    {
        $monthName = self::$nepaliMonths[$month] ?? '';
        return "{$day} {$monthName} {$year} BS";
    }

    public static function toNepaliNumerals(string $number): string
    {
        $map = ['0'=>'०','1'=>'१','2'=>'२','3'=>'३','4'=>'४',
                '5'=>'५','6'=>'६','7'=>'७','8'=>'८','9'=>'९'];
        return strtr((string)$number, $map);
    }

    public static function getDaysInMonth(int $bsYear, int $bsMonth): int
    {
        return self::$bsData[$bsYear][$bsMonth - 1] ?? 30;
    }

    public static function parseBsString(string $bsDate): array
    {
        $parts = explode('-', $bsDate);
        if (count($parts) !== 3) {
            throw new \InvalidArgumentException("Invalid BS date string: {$bsDate}");
        }
        return ['year' => (int)$parts[0], 'month' => (int)$parts[1], 'day' => (int)$parts[2]];
    }

    public static function carbonToBs(Carbon $date): array
    {
        return self::adToBs($date->year, $date->month, $date->day);
    }

    public static function carbonToBsString(Carbon $date): string
    {
        $bs = self::carbonToBs($date);
        return self::formatBs($bs['year'], $bs['month'], $bs['day']);
    }
